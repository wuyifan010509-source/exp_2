def generate_variations(seed_text: str, category: str) -> str:
    """基于种子文本生成变体"""
    # 简单的变体策略
    prefixes = [
        "请问", "麻烦问一下", "我想知道", "能不能告诉我",
        "", "", "", "",  # 空字符串增加多样性
    ]
    
    suffixes = [
        "？", "", "谢谢", "麻烦了", "急",
        "", "", "", "",
    ]
    
    # 随机添加前缀或后缀
    prefix = random.choice(prefixes)
    suffix = random.choice(suffixes)
    
    query = f"{prefix}{seed_text}{suffix}".strip()
    return query


def generate_validation_set(output_path: str = "validation_set.jsonl"):
    """生成验证集"""
    validation_data = []
    
    for category, config in CATEGORY_CONFIG.items():
        count = config["count"]
        cost = config["cost"]
        templates = SEED_TEMPLATES[category]
        
        print(f"生成 {category}: 目标 {count} 条, 模板 {len(templates)} 个")
        
        generated = []
        
        # 先使用所有模板
        for template in templates:
            if len(generated) < count:
                query = generate_variations(template, category)
                generated.append({
                    "query": query,
                    "category": category,
                    "cost_label": cost
                })
        
        # 如果模板不够，随机重复并变异
        while len(generated) < count:
            template = random.choice(templates)
            query = generate_variations(template, category)
            generated.append({
                "query": query,
                "category": category,
                "cost_label": cost
            })
        
        validation_data.extend(generated)
        print(f"  ✓ 完成 {category}: {len(generated)} 条")
    
    # 随机打乱
    random.shuffle(validation_data)
    
    # 保存
    with open(output_path, 'w', encoding='utf-8') as f:
        for item in validation_data:
            f.write(json.dumps(item, ensure_ascii=False) + '\n')
    
    # 统计报告
    print("\n" + "=" * 60)
    print("验证集生成完成！")
    print("=" * 60)
    
    from collections import Counter
    cat_counts = Counter(d['category'] for d in validation_data)
    cost_counts = Counter(d['cost_label'] for d in validation_data)
    
    print(f"\n总计: {len(validation_data)} 条")
    print("\n类别分布:")
    for cat in sorted(cat_counts.keys()):
        count = cat_counts[cat]
        target = CATEGORY_CONFIG[cat]["count"]
        cost = CATEGORY_CONFIG[cat]["cost"]
        print(f"  {cat:4s} (Cost={cost:4d}): {count:4d} 条 (目标 {target})")
    
    print("\nCost 分布:")
    for cost in sorted(cost_counts.keys()):
        count = cost_counts[cost]
        print(f"  Cost={cost:4d}: {count:4d} 条 ({count/len(validation_data)*100:5.1f}%)")
    
    print(f"\n输出文件: {output_path}")
    
    return validation_data


